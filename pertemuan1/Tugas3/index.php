<?php
// Operator perbandingan
// Equal (==)
echo 10 == 10;
echo '<br>';

// Identical (===)
echo '10' === 10;
echo '<br>';

// Not Equal (!=) / (<>)
echo 9 != 5;
echo 9 <> 5;
echo '<br>';

// Not identical 
echo '10' !== 11;
echo '<br>';

// Lebih dari (>)
echo 5 > 10;
echo '<br>';

// Kurang dari (<)
echo 5 < 10;
echo '<br>';

// Lebih dari atau sama dengan (>=)
echo 10 >= 10;
echo '<br>';

// Kurang dari atua sama dengan (<=)
echo 7 <= 7;
echo '<br>';

// Spaceship (<=>)
echo 15 <=> 10;
echo 5 <=> 10;
echo '<br>';

?>